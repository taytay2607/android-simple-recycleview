package com.example.a58010654.recyclelist;

public class User {
    private String name;
    private String id;
    private String age;

    public User(String name, String id, String age) {
        this.name = name;
        this.id = id;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public String getAge() {
        return age;
    }
}
